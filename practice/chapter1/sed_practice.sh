#!/bin/bash

# 該当する一文字を変更する
echo クロロエチルエチルエーテル | sed 's/エ/メ/' # s/検索対象/置換後の値/'

# 検索文字列を少し工夫
echo クロロエチルエチルエーテル | sed 's/エチルエ/エチルメ/' # s/検索対象/置換後の値/'

# 検索対象文字を全部置換
echo クロロメチルメチルエーテル | sed 's/メ/エ/g' # s/検索対象/置換後の値/g'

# 検索対象文字を置換後の値で使う
echo クロロエチルエーテル | sed 's/エチル/&&/' # &は検索対象に設定した文字列

# 建託対象の単語の順序を反転する
echo クロロメチルエチルエーテル | sed -E 's/(メチル)(エチル)/\2\1/' # -E : 拡張正規表現を使う　\数字；後方参照

# 画像ファイルの一括変換
time ls ../../qdata/2/img/*.png | sed 's/\.png$//' | xargs -P$(nproc) -I@ convert @.png @.jpg