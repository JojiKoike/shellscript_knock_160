#!/bin/bash

# 引数に指定した文字が１文字でも含まれている場合
seq 100 | grep "0" | xargs

# 引数に指定した文字で始まる文字を検索
seq 100 | grep "^8" | xargs

# 引数に指定した文字が末尾の文字列を検索
seq 100 | grep "8$" | xargs

# ある文字で始まる２文字を検索
seq 100 | grep "8." | xargs

# ある文字で始まり、後はある文字が0個以上続いて行末に達する文字を検索
seq 100 | grep "^10*$" | xargs

# 指定した候補のいずれかで行末が終わる文字を検索
seq 100 | grep "[02468]$" | xargs

# 指定した候補のいずれか以外で行末が終わる文字を検索
seq 100 | grep "[^02468]$" | xargs

# 先頭の文字で始まり先頭の文字で行末が終わる文字を検索
seq 100 | grep -E "^(.)\1$" | xargs